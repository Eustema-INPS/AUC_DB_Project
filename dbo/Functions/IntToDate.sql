
CREATE FUNCTION [dbo].[IntToDate](@DateInt INT)
RETURNS DATE
AS
 BEGIN
	RETURN CASE
		WHEN @DateInt IS NULL THEN CAST('19000101' AS DATE)
		WHEN @DateInt = 0 THEN CAST('19000101' AS DATE)
		WHEN @DateInt IS NOT NULL THEN DATEFROMPARTS(@DateInt / 10000, (@DateInt % 10000) / 100, @DateInt % 100) ELSE NULL END
 END
